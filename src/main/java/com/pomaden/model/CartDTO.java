package com.pomaden.model;

public class CartDTO {
	private int cart_idx;
	private String cart_member_id;
	private String cart_name;
	private String cart_img;
	private int cart_price;
	private int cart_sale;
	private String cart_category;
	private int cart_count;
	private String cart_color;
	private String cart_size;
	public int getCart_idx() {
		return cart_idx;
	}
	public void setCart_idx(int cart_idx) {
		this.cart_idx = cart_idx;
	}
	public String getCart_member_id() {
		return cart_member_id;
	}
	public void setCart_member_id(String cart_member_id) {
		this.cart_member_id = cart_member_id;
	}
	public String getCart_name() {
		return cart_name;
	}
	public void setCart_name(String cart_name) {
		this.cart_name = cart_name;
	}
	public String getCart_img() {
		return cart_img;
	}
	public void setCart_img(String cart_img) {
		this.cart_img = cart_img;
	}
	public int getCart_price() {
		return cart_price;
	}
	public void setCart_price(int cart_price) {
		this.cart_price = cart_price;
	}
	public int getCart_sale() {
		return cart_sale;
	}
	public void setCart_sale(int cart_sale) {
		this.cart_sale = cart_sale;
	}
	public String getCart_category() {
		return cart_category;
	}
	public void setCart_category(String cart_category) {
		this.cart_category = cart_category;
	}
	public int getCart_count() {
		return cart_count;
	}
	public void setCart_count(int cart_count) {
		this.cart_count = cart_count;
	}
	public String getCart_color() {
		return cart_color;
	}
	public void setCart_color(String cart_color) {
		this.cart_color = cart_color;
	}
	public String getCart_size() {
		return cart_size;
	}
	public void setCart_size(String cart_size) {
		this.cart_size = cart_size;
	}
	
	
}
